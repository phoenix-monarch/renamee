import re
import os
import time
import logging
from pyrogram.errors import FloodWait, RPCError

logging.basicConfig(level=logging.INFO, filename='error.log')
LOG = logging.getLogger("Bot by @YUITOAKASH")
LOG.setLevel(level=logging.INFO)

id_pattern = re.compile(r'^.\d+$')

class Config(object):
    # pyro client config
    API_ID = os.environ.get("API_ID", "")
    API_HASH = os.environ.get("API_HASH", "")
    BOT_TOKEN = os.environ.get("BOT_TOKEN", "")
    BOT_NAME = os.environ.get("BOT_NAME", "")
    TOKEN_TIMEOUT = int(os.environ.get("TOKEN_TIMEOUT", 86400))
    # database config
    DB_NAME = os.environ.get("DB_NAME","pyro-botz")
    DB_URL = os.environ.get("DB_URL","")
    # other configs
    BOT_UPTIME = time.time()
    START_PIC = os.environ.get("START_PIC", "")
    ADMIN = [int(admin) if id_pattern.search(admin) else admin for admin in os.environ.get('ADMIN', '').split()]
    FORCE_SUB = os.environ.get("FORCE_SUB", "")
    LOG_CHANNEL = int(os.environ.get("LOG_CHANNEL", None))
    # wes response configuration
    WEBHOOK = bool(os.environ.get("WEBHOOK", True))

    LOGGER = LOG
    shorteners_list = []

    def __init__(self):
        if os.path.exists('shorteners.txt'):
            with open('shorteners.txt', 'r') as f:
                lines = f.readlines()
                for line in lines:
                    temp = line.strip().split()
                    if len(temp) == 2:
                        self.shorteners_list.append({'domain': temp[0], 'api_key': temp[1]})

    async def sendMessage(client, message, text):
        try:
            return await message.reply_text(
                text=text,
                quote=True,
                disable_web_page_preview=True,
                disable_notification=True
            )
        except FloodWait as f:
            LOGGER.warning(str(f))
            await sleep(f.value * 1.2)
            return await sendMessage(client, message, text)
        except RPCError as e:
            LOGGER.error(f"{e.NAME}: {e.MESSAGE}")
        except Exception as e:
            LOGGER.error(str(e))
LOG.info('Config loaded successfully')


